export async function updateProfile(formData: FormData) {
  try {
    const { userId: ClerkId } = await auth();
    // destructuing userid that comes from auth and calling it celrkId
    if (!ClerkId) throw new Error("Unauthorized");
    // type assertion, to tell typsecript to treat the consts as string
    const name = formData.get("name") as string;
    const bio = formData.get("bio") as string;
    const location = formData.get("location") as string;
    const website = formData.get("website") as string;

    const user = await prisma.user.update({
      where: { ClerkId },
      data: {
        name,
        bio,
        location,
        website,
      },
    });

    revalidatePath("/profile");
    return { success: true, user };
  } catch (error) {
    console.log("error updating user", error);
    return { success: false, error: "Failure in updating user" };
  }
}

export async function isFollowing(userId: string) {
  try {
    const currentUserId = await getDbUserId();
    if (!currentUserId) return false;

    const follow = await prisma.follows.findUnique({
      where: {
        followerId_followingId: {
          followerId: currentUserId,
          followingId: userId,
        },
      },
    });
    //!! is a common javascript/typescript trick to convert a value into a boolean. pretty much like saying return follow !== null,but a better way to do it. nill becomes false.
    return !!follow;
  } catch (error) {
    console.log("Error checking follow status:", error);
    return false;
  }
}
